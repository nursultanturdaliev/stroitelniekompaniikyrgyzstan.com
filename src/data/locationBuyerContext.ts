export function getLocationBuyerContext(cityId?: number): LocationBuyerContext {
  if (cityId != null && byCityId[cityId]) return byCityId[cityId];
  return defaultContext;
}

export function getBishkekSubdistrictExtras(subdistrictNames: string[]): string[] {
  const out: string[] = [];
  const seen = new Set<string>();
  for (const raw of subdistrictNames) {
    const s = raw.trim().toLowerCase();
    if (!s) continue;
    for (const rule of bishkekSubdistrictExtra) {
      if (!rule.match(s)) continue;
      for (const b of rule.bullets) {
        if (!seen.has(b)) {
          seen.add(b);
          out.push(b);
        }
      }
    }
  }
  return out;
}
